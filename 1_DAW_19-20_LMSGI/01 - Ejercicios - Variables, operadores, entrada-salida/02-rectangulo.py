# Calcular a área e perímetro dun rectángulo.
print("Escribe a base do rectángulo:")
base = int(input())
print("Escribe a altura do rectángulo")
altura = int(input())
area = base * altura
perimetro = (base + altura) * 2
print("A área do rectángulo é", area)
print("O perímetro do rectángulo é", perimetro)